class Pessoa{

    getId(){
        return this.id;
    }

    setId(id){
        this.id = id;
    }

    getNome(){
        return this.nome;
    }

    setNome(name){
        this.nome = name;
    }

    getEmail(){
        return this.email;
    }
    
    setEmail(mail){
        this.email = mail;
    }

    getTelefone(){
        return this.telefone;
    }

    setTelefone(phone){
        this.telefone = phone;
    }

    setFoto(pic){
        this.foto = pic;
    }

    getFoto(){
        return this.foto;
    }
}